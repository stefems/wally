		<h1>Make Your Commits Meaningful with Emojis</h1>
		<p>Everyone loves to harp on worthless commit messages.</p>
		<img class="img-fluid" src="https://i.redd.it/87xckbodwwlx.png"/>
		<br>
		<p>
		It’s as if your drunk roommate rearranged everything in the kitchen and told you that it was for the best but doesn’t tell you where the bowls are located. You need your cereal, goddamit, and oh, great, we can’t find the mugs either -- WE’RE ALL GONNA DIE.
		</p>
		<p>
		Some have even gone to great lengths to make the commit messages <a href="https://pbs.twimg.com/media/Buc8yzHCYAAQpXy.jpg">interesting</a> with click-bait-esque titles, and by interesting we mean funny for MAYBE 2 commits. Okay, maybe for more than 2 commits. But surely there’s another approach?
		</p>
		<p>
		Emojis. The strange faces and images your mom and pops use incorrectly. Sometimes prospective significant others send them to you, but it's usually just a bit awkward. I got a tip from a fellow developer that told me that their team made a key to map emojis to meaningful commit actions. Here are some examples:
		</p>
		<p><span class="emoji">🚀</span>when improving performance</p>
		<p><span class="emoji">🐛</span>when squashing/reporting bugs</p>
		<p><span class="emoji">🔒</span>when working on security</p>
		<p><span class="emoji">💄</span>when developing UI</p>
		<p><span class="emoji">➕</span>when adding a feature</p>
		<p><span class="emoji">➖</span>when removing a feature</p>
		
		<p>
		Maybe you can pair them for extra meanings, too. A bug combined with security. A rocket, lipstick for UI, and a mobile phone. You can use a triangular flag for problematic code you’ve found, or a shower head for code cleanup. Hit this <a href="http://www.webpagefx.com/tools/emoji-cheat-sheet/">link</a> to see the emojis that are supported by github and many other platforms. Hit this <a href="https://gist.github.com/AliMD/3344523">link</a> if you’d just like to see emojis supported by github. If you’re confused as how to get the ":emojiNameGoesHere:" for the emoji, open the developer console or load the page via the “raw” button.
		</p>
